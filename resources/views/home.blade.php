Education Public School Varanasi is modeled on the concept of providing holistic education so as to promote an overall social, physical and mental development of each child. A meticulously planned and flawlessly executed curriculum forms the basic framework that helps shape each individual as per the learning style. The entire teaching learning pattern is child friendly and interactive, thus making the entire process engaging and creating learners for life.
